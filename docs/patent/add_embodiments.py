def renumber_for_two_new(text):
    """
    原有章节：四摘要 五权利要求 六在先对比 七确权证据 八附件说明
    插入两章后变为：六摘要 七权利要求 八在先对比 九确权证据 十附件说明
    必须从大数往小数替换，防止连锁错误。
    """
    text = text.replace('\n## 八、附件说明',    '\n## 十、附件说明')
    text = text.replace('\n## 七、确权证据',    '\n## 九、确权证据')
    text = text.replace('\n## 六、在先技术对比', '\n## 八、在先技术对比')
    text = text.replace('\n## 五、权利要求书',  '\n## 七、权利要求书')
    text = text.replace('\n## 四、摘要',        '\n## 六、摘要')
    return text

